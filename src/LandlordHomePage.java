    private void showDormPage(int page) {
    dormPanel.removeAll();

    currentPage = page;

    // 💥 main layout
    dormPanel.setLayout(new BorderLayout());

    JPanel gridPanel = new JPanel(new GridLayout(2, 2, 12, 12));
    gridPanel.setBackground(new Color(153, 204, 255));
    gridPanel.setBorder(BorderFactory.createEmptyBorder(12, 12, 12, 12));

    // ✅ ADD DORM CARD FIRST
    gridPanel.add(createAddDormCard());

    int start = (page - 1) * roomsPerPage;
    int end = Math.min(start + roomsPerPage, roomList.size());

    for (int i = start; i < end; i++) {
        Object[] data = roomList.get(i);

        int id = (int) data[0];
        String roomNo = (String) data[1];
        String dorm = (String) data[2];
        double rent = (double) data[3];
        String brgy = (String) data[4];
        String municipality = (String) data[5];

        gridPanel.add(createDormCard(id, roomNo, dorm, rent, brgy, municipality));
    }

    // 🔷 NAVIGATION
    JPanel navPanel = new JPanel();

    JButton prev = new JButton("Previous");
    JButton next = new JButton("Next");

    prev.setEnabled(page > 1);
    next.setEnabled(end < roomList.size());

    prev.addActionListener(e -> showDormPage(currentPage - 1));
    next.addActionListener(e -> showDormPage(currentPage + 1));

    navPanel.add(prev);
    navPanel.add(next);

    // 💥 attach
    dormPanel.add(gridPanel, BorderLayout.CENTER);
    dormPanel.add(navPanel, BorderLayout.SOUTH);

    dormPanel.revalidate();
    dormPanel.repaint();
}
    private JPanel createDormCard(int roomId, String roomNo, String dormName, double rent, String brgy, String municipality) {
    JPanel panel = new JPanel();
    panel.setPreferredSize(new Dimension(220, 220));
    panel.setLayout(new BorderLayout());
    panel.setBorder(BorderFactory.createLineBorder(Color.LIGHT_GRAY));
    panel.setBackground(Color.WHITE);

    int cardWidth = 180;
    int cardHeight = 200;
    int imageWidth = 224;
    int imageHeight = (int)(cardHeight * 0.7);

    // 🔷 IMAGE
    JLabel imgLabel = new JLabel();
    imgLabel.setHorizontalAlignment(JLabel.CENTER);

    ImageIcon img = getFirstImage(roomId);
    if (img != null) {
        Image scaled = img.getImage().getScaledInstance(imageWidth, imageHeight, Image.SCALE_SMOOTH);
        imgLabel.setIcon(new ImageIcon(scaled));
    }
    imgLabel.setPreferredSize(new Dimension(cardWidth, imageHeight));
    panel.add(imgLabel, BorderLayout.NORTH);

    // 🔷 INFO
    JPanel infoPanel = new JPanel();
    infoPanel.setLayout(new BoxLayout(infoPanel, BoxLayout.Y_AXIS));
    infoPanel.setBackground(Color.WHITE);
    infoPanel.setBorder(BorderFactory.createEmptyBorder(5, 10, 5, 10));

    JLabel title = new JLabel("Room " + roomNo + ", " + dormName);
    title.setFont(new Font("Arial", Font.BOLD, 13));

    JLabel location = new JLabel(brgy + ", " + municipality);
    location.setFont(new Font("Arial", Font.PLAIN, 12));

    JLabel price = new JLabel("₱ " + rent);
    price.setFont(new Font("Arial", Font.BOLD, 12));

    infoPanel.add(title);
    infoPanel.add(Box.createVerticalStrut(3));
    infoPanel.add(location);
    infoPanel.add(Box.createVerticalStrut(3));
    infoPanel.add(price);

    panel.add(infoPanel, BorderLayout.CENTER);

    // 🔷 BOTTOM BUTTON (CENTERED)
    JPanel bottomPanel = new JPanel(new FlowLayout(FlowLayout.CENTER));
    bottomPanel.setBackground(Color.WHITE);

    JButton seeMoreBtn = new JButton("See more");

    seeMoreBtn.addActionListener(e -> {
        // 👉 you can open another frame or dialog here
        JOptionPane.showMessageDialog(null, "Room ID: " + roomId);
    });

    bottomPanel.add(seeMoreBtn);

    panel.add(bottomPanel, BorderLayout.SOUTH);

    return panel;
}
    
