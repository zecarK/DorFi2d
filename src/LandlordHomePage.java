    private void loadName() {
    String query = "SELECT first_name FROM landlord_profile WHERE username = ?";

    try (Connection con = MyConnection.getConnection();
         PreparedStatement ps = con.prepareStatement(query)) {

        ps.setString(1, username);

        ResultSet rs = ps.executeQuery();
        
        roomManagementPanel.removeAll();


        roomManagementPanel.add(createAddRoomCard());

        if (rs.next()) {
            landlordName4.setText(rs.getString("first_name")); // ✅ direct
        } else {
            landlordName4.setText("No records");
        }

    } catch (SQLException ex) {
        ex.printStackTrace();
    }
}
   
    private void displayRoom() {
    roomList.clear();

    try {
        String query = "SELECT r.room_id, r.room_number, r.rent, d.dorm_name, l.brgy, l.municipality , l.location_id, d.dorm_id " +
    "FROM room_table r " +
    "JOIN dorm_building d ON r.dorm_id = d.dorm_id " +
    "JOIN location_table l ON d.location_id = l.location_id " +
    "WHERE r.username = ?";

        PreparedStatement ps = con.prepareStatement(query);
        ps.setString(1, username);
        ResultSet rs = ps.executeQuery();

        while (rs.next()) {
            roomList.add(new Object[]{
                rs.getInt("room_id"),
                rs.getString("room_number"),
                rs.getString("dorm_name"),
                rs.getDouble("rent"),
                rs.getString("brgy"),
                rs.getString("municipality"),
                rs.getInt("location_id"),
                rs.getInt("dorm_id")
            });
        }

    } catch (SQLException e) {
        e.printStackTrace();
    }

    showPage(1); // start at page 1
}
   private void showPage(int page) {
    roomManagementPanel.removeAll();

    currentPage = page;

    // 💥 STEP 1: change layout of main panel
    roomManagementPanel.setLayout(new BorderLayout());

    // 💥 STEP 2: create grid panel (this replaces your direct adds)
    JPanel gridPanel = new JPanel(new GridLayout(2, 2, 12, 12));
    gridPanel.setBackground(new Color(153, 204, 255));
    gridPanel.setOpaque(true);
    
    gridPanel.setBorder(BorderFactory.createEmptyBorder(12, 12, 12, 12));
    // ✅ ADD ROOM CARD FIRST
    gridPanel.add(createAddRoomCard());

    int start = (page - 1) * roomsPerPage;
    int end = Math.min(start + roomsPerPage, roomList.size());

    for (int i = start; i < end; i++) {
        Object[] data = roomList.get(i);

        int id = (int) data[0];
        String roomNo = (String) data[1];
        String dorm = (String) data[2];
        double rent = (double) data[3];
        String brgy = (String) data[4];
        String municipality = (String) data[5];
        int locationId = (int) data[6];
        int dormId = (int) data[7];

        // 💥 ADD TO GRID PANEL (NOT roomManagementPanel)
        gridPanel.add(createManageRoomCard(id, roomNo, dorm, rent, brgy, municipality,locationId,dormId));
    }

    // 💥 STEP 3: pagination panel (BOTTOM)
    JPanel navPanel = new JPanel();

    JButton prev = new JButton("Previous");
    JButton next = new JButton("Next");

    prev.setEnabled(page > 1);
    next.setEnabled(end < roomList.size());

    prev.addActionListener(e -> showPage(currentPage - 1));
    next.addActionListener(e -> showPage(currentPage + 1));

    navPanel.add(prev);
    navPanel.add(next);

    // 💥 STEP 4: add properly using BorderLayout
    roomManagementPanel.add(gridPanel, BorderLayout.CENTER);
    roomManagementPanel.add(navPanel, BorderLayout.SOUTH);

    roomManagementPanel.revalidate();
    roomManagementPanel.repaint();
}
      
       
    private ImageIcon getFirstImage(int roomId) {
    try {
        String q = "SELECT room_photo FROM room_image WHERE room_id = ? LIMIT 1";
        PreparedStatement ps = con.prepareStatement(q);
        ps.setInt(1, roomId);

        ResultSet rs = ps.executeQuery();

        if (rs.next()) {
            String path = rs.getString("room_photo");
            return new ImageIcon(path);
        }

    } catch (Exception e) {
        e.printStackTrace();
    }
    return null;
}
private void deleteRecord(int roomId, int dormId, int locationId, Connection con) {
     try {
        con.setAutoCommit(false);

        // 🔥 delete child tables first
        PreparedStatement ps;

        ps = con.prepareStatement("DELETE FROM room_image WHERE room_id = ?");
        ps.setInt(1, roomId);
        ps.executeUpdate();
        ps.close();

        ps = con.prepareStatement("DELETE FROM room_facility WHERE room_id = ?");
        ps.setInt(1, roomId);
        ps.executeUpdate();
        ps.close();

        // 🔥 delete room
        ps = con.prepareStatement("DELETE FROM room_table WHERE room_id = ?");
        ps.setInt(1, roomId);
        ps.executeUpdate();
        ps.close();

        // ⚠️ ONLY delete dorm if no more rooms exist
        ps = con.prepareStatement("SELECT COUNT(*) FROM room_table WHERE dorm_id = ?");
        ps.setInt(1, dormId);
        ResultSet rs = ps.executeQuery();

        if (rs.next() && rs.getInt(1) == 0) {
            ps = con.prepareStatement("DELETE FROM dorm_building WHERE dorm_id = ?");
            ps.setInt(1, dormId);
            ps.executeUpdate();
            ps.close();

            // ⚠️ ONLY delete location if no more dorms exist
            ps = con.prepareStatement("SELECT COUNT(*) FROM dorm_building WHERE location_id = ?");
            ps.setInt(1, locationId);
            rs = ps.executeQuery();

            if (rs.next() && rs.getInt(1) == 0) {
                ps = con.prepareStatement("DELETE FROM location_table WHERE location_id = ?");
                ps.setInt(1, locationId);
                ps.executeUpdate();
                ps.close();
            }
        }

        con.commit();

    } catch (SQLException ex) {
        try {
            con.rollback();
        } catch (SQLException e) {
            e.printStackTrace();
        }
        ex.printStackTrace();
    } finally {
        try {
            con.setAutoCommit(true);
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
}
private void deleteRoomCard(int roomId, int dormId, int locationId) {
    int option = JOptionPane.showConfirmDialog(
            this,
            "Are you sure you want to delete this record?",
            "Confirm Delete",
            JOptionPane.YES_NO_OPTION
    );

    if (option == JOptionPane.YES_OPTION) {
        // 🔥 Delete from database
        deleteRecord(roomId, dormId, locationId, con);

        // 🔥 Remove from local list (VERY IMPORTANT)
        roomList.removeIf(room -> (int) room[0] == roomId);

        JOptionPane.showMessageDialog(this, "Record deleted successfully.");

        // 🔥 Refresh UI (stay on same page if possible)
        int maxPage = (int) Math.ceil((double) roomList.size() / roomsPerPage);
        if (currentPage > maxPage) {
            currentPage = Math.max(1, maxPage);
        }

        showPage(currentPage);
    }
}
